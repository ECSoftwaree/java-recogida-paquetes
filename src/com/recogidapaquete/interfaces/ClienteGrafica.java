    private void lblVerPerfilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblVerPerfilMouseClicked
        
        if (comprobarUsuarioNulos == 1) {
            this.user = null;
        }
        
        if (user == null) {
            comprobarUsuarioNulos = 1;
            String user = JOptionPane.showInputDialog(null, "Ingrese el usuario del cliente para ver\nsu perfil.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            this.user = user;
        }
        mostrarClientes();
        
    }//GEN-LAST:event_lblVerPerfilMouseClicked

