    public void mostrarClientes(){
        listaClientes = conexionMySQL.verPerfilUsuario(user);

        for (Cliente cliente: listaClientes) {
            lblNombre.setText(cliente.getNombre());
            lblApellido.setText(cliente.getApellido());
            lblDireccion.setText(cliente.getDireccion());
            lblCorreo.setText(cliente.getCorreo());
            lblSexo.setText(cliente.getSexo());
        }
    }
    
    public void mostrarPaquetes(){
        listaPaquetes = conexionMySQL.verPaquetesDePerfilUsuario(user);
        dtm = (DefaultTableModel) jtVerPaquetes.getModel();
        
        dtm.setNumRows(0);
        for (int i=0;i<listaPaquetes.size();i++){
            Object [] row = {listaPaquetes.get(i).getDescripcion(),
            listaPaquetes.get(i).getTipoPaquete(),listaPaquetes.get(i).getEstadoValido(),listaPaquetes.get(i).getUbicacion(),listaPaquetes.get(i).getFecha()};
            dtm.addRow(row);
        }
    }
    
